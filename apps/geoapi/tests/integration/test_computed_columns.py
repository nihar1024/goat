class TestApplyFieldConfigToProperties:
    """Unit tests for the pure helper that enriches queryables properties."""

    def test_kind_from_json_type_numeric(self) -> None:
        """number and integer JSON types map to kind 'number'."""
        assert _kind_from_json_type("number") == "number"
        assert _kind_from_json_type("integer") == "number"

    def test_kind_from_json_type_string_fallback(self) -> None:
        """Any non-numeric JSON type maps to kind 'string'."""
        for t in ("string", "boolean", "object", "geometry"):
            assert _kind_from_json_type(t) == "string", f"Failed for type={t!r}"

    def test_augments_with_explicit_field_config(self) -> None:
        """Properties whose names appear in field_config get their metadata."""
        properties: dict = {
            "area_m2": {"name": "area_m2", "type": "number"},
            "label": {"name": "label", "type": "string"},
        }
        field_config = {
            "area_m2": {
                "kind": "area",
                "is_computed": True,
                "display_config": {"unit": "m²"},
            }
        }
        _apply_field_config_to_properties(properties, field_config)

        assert properties["area_m2"]["kind"] == "area"
        assert properties["area_m2"]["is_computed"] is True
        assert properties["area_m2"]["display_config"] == {"unit": "m²"}

    def test_fallback_for_columns_without_field_config(self) -> None:
        """Columns absent from field_config get sensible defaults."""
        properties: dict = {
            "score": {"name": "score", "type": "number"},
            "name": {"name": "name", "type": "string"},
        }
        _apply_field_config_to_properties(properties, {})

        assert properties["score"]["kind"] == "number"
        assert properties["score"]["is_computed"] is False
        assert properties["score"]["display_config"] == {}

        assert properties["name"]["kind"] == "string"
        assert properties["name"]["is_computed"] is False
        assert properties["name"]["display_config"] == {}

    def test_geometry_ref_entry_is_skipped(self) -> None:
        """Geometry $ref entries must not be augmented with kind/is_computed."""
        properties: dict = {
            "geom": {"$ref": "https://geojson.org/schema/Geometry.json"},
            "name": {"name": "name", "type": "string"},
        }
        _apply_field_config_to_properties(properties, {})

        # geom entry untouched — no extra keys injected
        assert "kind" not in properties["geom"]
        assert "is_computed" not in properties["geom"]
        assert "display_config" not in properties["geom"]

        # name entry still augmented
        assert properties["name"]["kind"] == "string"

    def test_existing_json_type_preserved(self) -> None:
        """Adding kind/is_computed/display_config must not drop the 'type' key."""
        properties: dict = {
            "count": {"name": "count", "type": "integer"},
        }
        _apply_field_config_to_properties(properties, {})

        assert properties["count"]["type"] == "integer"
        assert properties["count"]["kind"] == "number"  # inferred from integer

    def test_field_config_kind_takes_priority_over_inferred(self) -> None:
        """An explicit kind in field_config overrides the inferred kind."""
        properties: dict = {
            "pop_density": {"name": "pop_density", "type": "number"},
        }
        field_config = {
            "pop_density": {
                "kind": "density",
                "is_computed": False,
                "display_config": {},
            }
        }
        _apply_field_config_to_properties(properties, field_config)

        assert properties["pop_density"]["kind"] == "density"
