"""Function registry for the Formula Builder.

This module defines all allowed functions that can be used in expressions.
Each function has metadata including syntax, examples, and localized descriptions.
"""

from dataclasses import dataclass, field
from enum import Enum


class FunctionCategory(str, Enum):
    """Categories for organizing functions in the UI."""

    MATH = "math"
    STRING = "string"
    DATE = "date"
    AGGREGATE = "aggregate"
    WINDOW = "window"
    SPATIAL = "spatial"
    CONDITIONAL = "conditional"


@dataclass
class FunctionParameter:
    """A parameter for a function."""

    name: str
    type: str  # e.g., "NUMERIC", "VARCHAR", "ANY"
    description_key: str  # i18n key like "param_x"
    optional: bool = False
    default: str | None = None


@dataclass
class FunctionDoc:
    """Documentation for a single function."""

    name: str
    syntax: str
    example: str
    category: FunctionCategory
    description_key: str  # i18n key like "abs" -> t("fn.abs.description")
    return_type: str
    parameters: list[FunctionParameter] = field(default_factory=list)
    min_args: int = 0
    max_args: int | None = None  # None = unlimited
    requires_over: bool = False  # True for window functions


