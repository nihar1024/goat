def get_allowed_functions() -> set[str]:
    """Get the set of all allowed function names (lowercase)."""
    return set(FUNCTION_REGISTRY.keys())


def get_functions_by_category() -> dict[FunctionCategory, list[FunctionDoc]]:
    """Get functions grouped by category."""
    result: dict[FunctionCategory, list[FunctionDoc]] = {
        cat: [] for cat in FunctionCategory
    }
    for func in FUNCTION_REGISTRY.values():
        result[func.category].append(func)
    # Sort each category alphabetically
    for cat in result:
        result[cat].sort(key=lambda f: f.name)
    return result


def get_function_names_set() -> set[str]:
    """Get lowercase function names for validation."""
    return {name.lower() for name in FUNCTION_REGISTRY.keys()}


